gl::vertex_array_object gltf::primitives::make_cube()
{
    gl::vertex_array_object vao;
    gl::buffer<GL_ARRAY_BUFFER> vbo;
    vbo.fill(cube_vertices, sizeof(cube_vertices));

    vao.add_vertex_array(vbo, 3, sizeof(float[3 + 3 + 2]));
    vao.add_vertex_array(vbo, 3, sizeof(float[3 + 3 + 2]), sizeof(float[3]));
    vao.add_vertex_array(vbo, 2, sizeof(float[3 + 3 + 2]), sizeof(float[3 + 3]));

    return vao;
}


gl::program gltf::primitives::make_cubemap_shader()
{
    return gl::program(gl::shader<GL_VERTEX_SHADER>(shaders::cubemap_bg_vss), gl::shader<GL_FRAGMENT_SHADER>(shaders::cubemap_bg_fss));
}
